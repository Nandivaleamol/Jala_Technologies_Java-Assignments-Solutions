package Java_02_Operators;

//3. Program to equal operator and not equal operators
public class Problem03 {
    public static void main(String[] args) {

        int a = 10;
        int b = 8;

        boolean c = b==a;

        System.out.println(a+"=="+b+"="+c);

        boolean d = b!=a;

        System.out.println(a+"!="+b+"="+d);
    }
}
