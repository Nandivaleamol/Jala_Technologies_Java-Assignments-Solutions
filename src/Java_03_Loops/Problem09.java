//9. Write a program to find the prime or not.
public class Problem09 {
    public static void main(String[] args) {

        int n = 5;
        int i = 2;

        if(n%i==0){

            System.out.println(n+" is prime number");
        }
        else{
            System.out.println(n+" is not prime number");
        }

    }
}
